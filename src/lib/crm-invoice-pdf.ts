// ════════════════════════════════════════════════════════════════════════
// crm-invoice-pdf.ts
//
// Generates a client-ready PDF for a CRM invoice. Uses pdf-lib (same
// engine as src/lib/audit-trail-pdf.ts) so it runs on Vercel's node
// runtime without Chromium. Layout:
//
//   ┌────────────────────────────────────────────────────────┐
//   │  [logo]                         INVOICE                │
//   │  Firm name                      MP-2026-0042           │
//   │  Firm address                   Issue: 15 Apr 2026     │
//   │  Firm VAT · matricule           Due:   15 May 2026     │
//   ├────────────────────────────────────────────────────────┤
//   │  Bill to                                               │
//   │  Client name                                           │
//   │  Client address                                        │
//   │  Matter: MP-2026-0001 · Fund formation advice          │
//   ├────────────────────────────────────────────────────────┤
//   │  # │ Description              │ Qty │ Rate │  Amount  │
//   ├────┼──────────────────────────┼─────┼──────┼──────────┤
//   │  1 │ …                        │  … │  …  │       … │
//   ├────┴──────────────────────────┴─────┴──────┴──────────┤
//   │                          Subtotal (excl. VAT)  €X,XXX │
//   │                          VAT (17%)              €  XX │
//   │                          ─────────────────────────────│
//   │                          TOTAL                 €X,XXX │
//   ├────────────────────────────────────────────────────────┤
//   │  Payment instructions                                  │
//   │  Bank · IBAN · BIC · Reference: MP-2026-0042           │
//   │                                                        │
//   │  Footer text (firm terms / boilerplate)                │
//   └────────────────────────────────────────────────────────┘
//
// The PDF intentionally has NO "generated by cifra" branding — it's
// the firm's commercial document, not ours.
// ════════════════════════════════════════════════════════════════════════

import { PDFDocument, StandardFonts, rgb, type PDFFont, type PDFPage } from 'pdf-lib';
import { queryOne } from '@/lib/db';
import { getFirmSettings, type FirmSettings } from '@/lib/crm-firm-settings';

const NAVY   = rgb(0x1a / 255, 0x1a / 255, 0x2e / 255);
const GREY   = rgb(0x6b / 255, 0x72 / 255, 0x80 / 255);
const DARK   = rgb(0x11 / 255, 0x18 / 255, 0x27 / 255);
const BORDER = rgb(0xe5 / 255, 0xe7 / 255, 0xeb / 255);
const STRIPE = rgb(0xfa / 255, 0xfa / 255, 0xfb / 255);
const BRAND  = rgb(0x0f / 255, 0x3d / 255, 0x56 / 255);   // navy/teal — firm-neutral

const PAGE_W = 595.28;   // A4
const PAGE_H = 841.89;
const MARGIN = 50;

type FontSet = { regular: PDFFont; bold: PDFFont; oblique: PDFFont };

interface LineItem {
  description?: string;
  quantity?: number | string;
  rate?: number | string;
  amount?: number | string;
  // Some imports store 'hours' + 'rate_eur' — we tolerate both shapes.
  hours?: number | string;
  rate_eur?: number | string;
}

interface InvoiceRow {
  id: string;
  invoice_number: string;
  issue_date: string | null;
  due_date: string | null;
  currency: string | null;
  amount_excl_vat: number | string | null;
  vat_rate: number | string | null;
  vat_amount: number | string | null;
  amount_incl_vat: number | string | null;
  amount_paid: number | string | null;
  status: string;
  payment_method: string | null;
  payment_reference: string | null;
  line_items: unknown;
  notes: string | null;
  client_name: string | null;
  client_address_billing: string | null;
  client_vat: string | null;
  matter_reference: string | null;
  matter_title: string | null;
}

export interface InvoicePDFResult {
  buffer: Buffer;
  filename: string;
}

/**
 * Build a PDF for a single CRM invoice. Queries the invoice + its
 * client + its matter in one round-trip so the PDF is consistent with
 * what the user saw when they clicked "Download".
 */
export async function buildInvoicePDF(invoiceId: string): Promise<InvoicePDFResult> {
  const [inv, firm] = await Promise.all([
    queryOne<InvoiceRow>(
      `SELECT b.id, b.invoice_number, b.issue_date::text AS issue_date,
              b.due_date::text AS due_date, b.currency,
              b.amount_excl_vat, b.vat_rate, b.vat_amount, b.amount_incl_vat,
              b.amount_paid, b.status, b.payment_method, b.payment_reference,
              b.line_items, b.notes,
              c.company_name AS client_name,
              c.billing_address AS client_address_billing,
              c.vat_number AS client_vat,
              m.matter_reference, m.title AS matter_title
         FROM crm_billing_invoices b
         LEFT JOIN crm_companies c ON c.id = b.company_id
         LEFT JOIN crm_matters   m ON m.id = b.matter_id
        WHERE b.id = $1`,
      [invoiceId],
    ),
    getFirmSettings(),
  ]);
  if (!inv) throw new Error('Invoice not found');

  const lineItems = normaliseLineItems(inv.line_items);
  const ccy = inv.currency ?? 'EUR';

  const pdf = await PDFDocument.create();
  pdf.setTitle(`Invoice ${inv.invoice_number}`);
  pdf.setAuthor(firm.firm_name);
  pdf.setSubject(`Invoice ${inv.invoice_number} · ${firm.firm_name}`);
  pdf.setCreator(firm.firm_name);

  const fonts: FontSet = {
    regular: await pdf.embedFont(StandardFonts.Helvetica),
    bold:    await pdf.embedFont(StandardFonts.HelveticaBold),
    oblique: await pdf.embedFont(StandardFonts.HelveticaOblique),
  };

  let page = pdf.addPage([PAGE_W, PAGE_H]);

  // ─── Header ─────────────────────────────────────────────
  let y = drawHeader(page, fonts, firm, inv);

  // ─── Bill-to block ──────────────────────────────────────
  y = drawBillTo(page, fonts, inv, y);

  // ─── Line items table ───────────────────────────────────
  const tableResult = drawLineItemsTable(pdf, page, fonts, lineItems, ccy, y);
  page = tableResult.page;
  y = tableResult.y;

  // ─── Totals ─────────────────────────────────────────────
  y = drawTotals(page, fonts, inv, ccy, y);

  // ─── Payment instructions ───────────────────────────────
  y = drawPaymentInstructions(page, fonts, firm, inv, y);

  // ─── Notes (if any) ─────────────────────────────────────
  if (inv.notes && inv.notes.trim()) {
    y = drawNotes(page, fonts, inv.notes, y);
  }

  // ─── Footer ─────────────────────────────────────────────
  drawFooter(page, fonts, firm);

  const bytes = await pdf.save();
  return {
    buffer: Buffer.from(bytes),
    filename: `${sanitiseFilename(inv.invoice_number)}.pdf`,
  };
}

