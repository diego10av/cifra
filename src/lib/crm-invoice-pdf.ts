// Helpers
// ═══════════════════════════════════════════════════════════════════

function drawHeader(page: PDFPage, fonts: FontSet, firm: FirmSettings, inv: InvoiceRow): number {
  // Left: firm identity.
  let ly = PAGE_H - MARGIN;
  page.drawText(firm.firm_name, { x: MARGIN, y: ly, size: 16, font: fonts.bold, color: BRAND });
  ly -= 20;

  for (const line of firm.firm_address_lines) {
    page.drawText(line, { x: MARGIN, y: ly, size: 9, font: fonts.regular, color: DARK });
    ly -= 11;
  }
  const metaBits: string[] = [];
  if (firm.firm_vat_number) metaBits.push(`VAT ${firm.firm_vat_number}`);
  if (firm.firm_matricule)  metaBits.push(`Matricule ${firm.firm_matricule}`);
  if (firm.firm_rcs_number) metaBits.push(`RCS ${firm.firm_rcs_number}`);
  if (metaBits.length > 0) {
    page.drawText(metaBits.join('  ·  '), { x: MARGIN, y: ly, size: 8, font: fonts.regular, color: GREY });
    ly -= 11;
  }
  const contactBits: string[] = [];
  if (firm.firm_email)   contactBits.push(firm.firm_email);
  if (firm.firm_phone)   contactBits.push(firm.firm_phone);
  if (firm.firm_website) contactBits.push(firm.firm_website);
  if (contactBits.length > 0) {
    page.drawText(contactBits.join('  ·  '), { x: MARGIN, y: ly, size: 8, font: fonts.regular, color: GREY });
    ly -= 11;
  }

  // Right: INVOICE block.
  const rightX = PAGE_W - MARGIN;
  let ry = PAGE_H - MARGIN;
  const titleW = fonts.bold.widthOfTextAtSize('INVOICE', 20);
  page.drawText('INVOICE', { x: rightX - titleW, y: ry, size: 20, font: fonts.bold, color: DARK });
  ry -= 24;

  const numLabel = 'No.';
  const num = inv.invoice_number;
  const numW = fonts.bold.widthOfTextAtSize(num, 12);
  page.drawText(numLabel, { x: rightX - numW - 30, y: ry, size: 9, font: fonts.regular, color: GREY });
  page.drawText(num, { x: rightX - numW, y: ry, size: 12, font: fonts.bold, color: DARK });
  ry -= 16;

  if (inv.issue_date) {
    ry = drawLabelValueRight(page, fonts, 'Issue date', formatDate(inv.issue_date), rightX, ry);
  }
  if (inv.due_date) {
    ry = drawLabelValueRight(page, fonts, 'Due date', formatDate(inv.due_date), rightX, ry);
  }
  if (inv.status && inv.status !== 'draft') {
    const statusLabel = inv.status.toUpperCase().replace(/_/g, ' ');
    ry = drawLabelValueRight(page, fonts, 'Status', statusLabel, rightX, ry);
  }

  return Math.min(ly, ry) - 14;
}

function drawLabelValueRight(
  page: PDFPage, fonts: FontSet, label: string, value: string, rightX: number, y: number,
): number {
  const valW = fonts.regular.widthOfTextAtSize(value, 9);
  page.drawText(label, { x: rightX - valW - fonts.regular.widthOfTextAtSize(label, 8) - 8, y, size: 8, font: fonts.regular, color: GREY });
  page.drawText(value, { x: rightX - valW, y, size: 9, font: fonts.regular, color: DARK });
  return y - 12;
}

function drawBillTo(page: PDFPage, fonts: FontSet, inv: InvoiceRow, startY: number): number {
  let y = startY;
  // Divider
  page.drawLine({ start: { x: MARGIN, y }, end: { x: PAGE_W - MARGIN, y }, thickness: 0.5, color: BORDER });
  y -= 16;

  page.drawText('BILL TO', { x: MARGIN, y, size: 8, font: fonts.bold, color: GREY });
  y -= 14;

  if (inv.client_name) {
    page.drawText(inv.client_name, { x: MARGIN, y, size: 11, font: fonts.bold, color: DARK });
    y -= 14;
  }
  if (inv.client_address_billing) {
    for (const line of String(inv.client_address_billing).split(/\r?\n/).filter(Boolean)) {
      page.drawText(line, { x: MARGIN, y, size: 9, font: fonts.regular, color: DARK });
      y -= 11;
    }
  }
  if (inv.client_vat) {
    page.drawText(`VAT ${inv.client_vat}`, { x: MARGIN, y, size: 8, font: fonts.regular, color: GREY });
    y -= 11;
  }

  // Matter reference on the right column
  if (inv.matter_reference) {
    const mRef = inv.matter_reference;
    const mTitle = inv.matter_title ?? '';
    const col2X = PAGE_W / 2 + 20;
    let my = startY - 16 - 14;
    page.drawText('MATTER', { x: col2X, y: my + 14, size: 8, font: fonts.bold, color: GREY });
    page.drawText(mRef, { x: col2X, y: my, size: 11, font: fonts.bold, color: DARK });
    my -= 14;
    if (mTitle) {
      const wrapped = wrapText(fonts.regular, 9, mTitle, PAGE_W - MARGIN - col2X);
      for (const line of wrapped) {
        page.drawText(line, { x: col2X, y: my, size: 9, font: fonts.regular, color: DARK });
        my -= 11;
      }
    }
  }

  return y - 10;
}

interface TableResult { page: PDFPage; y: number; }

function drawLineItemsTable(
  pdf: PDFDocument, pageIn: PDFPage, fonts: FontSet, items: LineItem[], ccy: string, startY: number,
): TableResult {
  let page = pageIn;
  let y = startY;

  const cols = [
    { key: '#',        w: 25,  align: 'left'  as const },
    { key: 'Description', w: 285, align: 'left'  as const },
    { key: 'Qty',      w: 50,  align: 'right' as const },
    { key: 'Rate',     w: 65,  align: 'right' as const },
    { key: 'Amount',   w: 70,  align: 'right' as const },
  ];
  const tableX = MARGIN;
  const tableW = cols.reduce((s, c) => s + c.w, 0);
  const rowH = 20;
  const headerH = 22;

  // Header row
  page.drawRectangle({ x: tableX, y: y - headerH, width: tableW, height: headerH, color: rgb(0.95, 0.96, 0.98) });
  let cx = tableX + 8;
  for (const c of cols) {
    const text = c.key.toUpperCase();
    const textW = fonts.bold.widthOfTextAtSize(text, 8);
    const tx = c.align === 'right' ? cx + c.w - textW - 16 : cx;
    page.drawText(text, { x: tx, y: y - 14, size: 8, font: fonts.bold, color: GREY });
    cx += c.w;
  }
  y -= headerH;

  // Rows
  items.forEach((it, i) => {
    if (y - rowH < MARGIN + 180) {
      // Not enough room for row + totals block — new page
      page = pdf.addPage([PAGE_W, PAGE_H]);
      y = PAGE_H - MARGIN - 20;
    }
    if (i % 2 === 1) {
      page.drawRectangle({ x: tableX, y: y - rowH, width: tableW, height: rowH, color: STRIPE });
    }

    const qty = numOrNull(it.quantity ?? it.hours);
    const rate = numOrNull(it.rate ?? it.rate_eur);
    const amount = numOrNull(it.amount) ?? (qty !== null && rate !== null ? qty * rate : null);

    const values = [
      String(i + 1),
      truncate(String(it.description ?? '—'), 60),
      qty !== null ? formatQty(qty) : '',
      rate !== null ? formatMoney(rate, ccy) : '',
      amount !== null ? formatMoney(amount, ccy) : '',
    ];

    let cx2 = tableX + 8;
    cols.forEach((c, ci) => {
      const text = values[ci];
      const textW = fonts.regular.widthOfTextAtSize(text, 9);
      const tx = c.align === 'right' ? cx2 + c.w - textW - 16 : cx2;
      page.drawText(text, { x: tx, y: y - 13, size: 9, font: fonts.regular, color: DARK });
      cx2 += c.w;
    });
    y -= rowH;
  });

  if (items.length === 0) {
    page.drawText('(No line items)', { x: tableX + 8, y: y - 13, size: 9, font: fonts.oblique, color: GREY });
    y -= rowH;
  }

  // Bottom border
  page.drawLine({ start: { x: tableX, y }, end: { x: tableX + tableW, y }, thickness: 0.5, color: BORDER });

  return { page, y: y - 14 };
}

function drawTotals(page: PDFPage, fonts: FontSet, inv: InvoiceRow, ccy: string, startY: number): number {
  const rightX = PAGE_W - MARGIN;
  let y = startY;

  const excl = numOrNull(inv.amount_excl_vat) ?? 0;
  const vatRate = numOrNull(inv.vat_rate);
  const vatAmt = numOrNull(inv.vat_amount) ?? 0;
  const incl = numOrNull(inv.amount_incl_vat) ?? (excl + vatAmt);
  const paid = numOrNull(inv.amount_paid) ?? 0;
  const outstanding = incl - paid;

  y = drawTotalLine(page, fonts, 'Subtotal (excl. VAT)', formatMoney(excl, ccy), rightX, y, false);
  const vatLabel = vatRate !== null ? `VAT (${(vatRate * 100).toFixed(0)}%)` : 'VAT';
  y = drawTotalLine(page, fonts, vatLabel, formatMoney(vatAmt, ccy), rightX, y, false);

  // Divider above the grand total
  page.drawLine({ start: { x: PAGE_W / 2, y: y + 4 }, end: { x: rightX, y: y + 4 }, thickness: 0.5, color: BORDER });

  y = drawTotalLine(page, fonts, 'TOTAL', formatMoney(incl, ccy), rightX, y, true);

  if (paid > 0) {
    y = drawTotalLine(page, fonts, 'Paid to date', `−${formatMoney(paid, ccy)}`, rightX, y, false);
    page.drawLine({ start: { x: PAGE_W / 2, y: y + 4 }, end: { x: rightX, y: y + 4 }, thickness: 0.5, color: BORDER });
    y = drawTotalLine(page, fonts, 'OUTSTANDING', formatMoney(outstanding, ccy), rightX, y, true);
  }

  return y - 10;
}

function drawTotalLine(
  page: PDFPage, fonts: FontSet, label: string, value: string, rightX: number, y: number, bold: boolean,
): number {
  const size = bold ? 11 : 9;
  const font = bold ? fonts.bold : fonts.regular;
  const color = bold ? DARK : DARK;
  const valW = font.widthOfTextAtSize(value, size);
  const labelW = font.widthOfTextAtSize(label, size);
  page.drawText(label, { x: rightX - valW - labelW - 20, y, size, font, color: bold ? DARK : GREY });
  page.drawText(value, { x: rightX - valW, y, size, font, color });
  return y - (bold ? 16 : 13);
}

function drawPaymentInstructions(
  page: PDFPage, fonts: FontSet, firm: FirmSettings, inv: InvoiceRow, startY: number,
): number {
  let y = startY;
  page.drawLine({ start: { x: MARGIN, y }, end: { x: PAGE_W - MARGIN, y }, thickness: 0.5, color: BORDER });
  y -= 14;

  page.drawText('PAYMENT INSTRUCTIONS', { x: MARGIN, y, size: 8, font: fonts.bold, color: GREY });
  y -= 14;

  const lines: string[] = [];
  if (firm.bank_name) lines.push(`Bank: ${firm.bank_name}`);
  if (firm.bank_iban) lines.push(`IBAN: ${firm.bank_iban}`);
  if (firm.bank_bic)  lines.push(`BIC:  ${firm.bank_bic}`);
  const ref = inv.payment_reference ?? inv.invoice_number;
  lines.push(`Reference: ${ref}`);
  if (firm.payment_terms_days) {
    lines.push(`Payment terms: ${firm.payment_terms_days} days from issue date.`);
  }

  for (const line of lines) {
    page.drawText(line, { x: MARGIN, y, size: 9, font: fonts.regular, color: DARK });
    y -= 12;
  }
  return y - 4;
}

function drawNotes(page: PDFPage, fonts: FontSet, notes: string, startY: number): number {
  let y = startY - 6;
  page.drawText('NOTES', { x: MARGIN, y, size: 8, font: fonts.bold, color: GREY });
  y -= 13;
  const wrapped = wrapText(fonts.regular, 9, notes, PAGE_W - MARGIN * 2);
  for (const line of wrapped) {
    if (y < MARGIN + 30) break;    // avoid smashing into footer
    page.drawText(line, { x: MARGIN, y, size: 9, font: fonts.regular, color: DARK });
    y -= 11;
  }
  return y - 6;
}

function drawFooter(page: PDFPage, fonts: FontSet, firm: FirmSettings): void {
  // Footer text from settings (firm's legal boilerplate)
  if (firm.footer_text) {
    const wrapped = wrapText(fonts.oblique, 7, firm.footer_text, PAGE_W - MARGIN * 2);
    let y = 40;
    for (const line of wrapped.slice(0, 3)) {
      const tw = fonts.oblique.widthOfTextAtSize(line, 7);
      page.drawText(line, { x: (PAGE_W - tw) / 2, y, size: 7, font: fonts.oblique, color: GREY });
      y -= 9;
    }
  }
  // Page frame at the very bottom is intentionally omitted — a tidy
  // invoice needs air, not lines.
}

// ─── Utilities ─────────────────────────────────────────────

function normaliseLineItems(raw: unknown): LineItem[] {
  if (!Array.isArray(raw)) return [];
  return raw.filter((x): x is LineItem => x && typeof x === 'object');
}

function numOrNull(v: unknown): number | null {
  if (v === null || v === undefined || v === '') return null;
  const n = Number(v);
  return Number.isFinite(n) ? n : null;
}

function formatMoney(n: number, ccy: string): string {
  const sign = ccy === 'EUR' ? '€' : ccy === 'GBP' ? '£' : ccy === 'USD' ? '$' : '';
  const body = n.toLocaleString('en-GB', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
  return sign ? `${sign}${body}` : `${body} ${ccy}`;
}

function formatQty(n: number): string {
  // Integer if whole, else up to 2 decimals.
  return Number.isInteger(n) ? String(n) : n.toFixed(2);
}

function formatDate(iso: string): string {
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return iso;
  return d.toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' });
}

function wrapText(font: PDFFont, size: number, text: string, maxWidth: number): string[] {
  const out: string[] = [];
  for (const paragraph of text.split(/\r?\n/)) {
    const words = paragraph.split(/\s+/).filter(Boolean);
    let line = '';
    for (const w of words) {
      const candidate = line ? `${line} ${w}` : w;
      if (font.widthOfTextAtSize(candidate, size) <= maxWidth) {
        line = candidate;
      } else {
        if (line) out.push(line);
        line = w;
      }
    }
    if (line) out.push(line);
    if (!words.length) out.push('');
  }
  return out;
}

function truncate(s: string, max: number): string {
  if (s.length <= max) return s;
  return s.slice(0, max - 1) + '…';
}

function sanitiseFilename(s: string): string {
  return s.replace(/[^a-zA-Z0-9._-]+/g, '_');
}

